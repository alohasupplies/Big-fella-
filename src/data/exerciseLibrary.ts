// Helper function to get exercises by muscle group
export const getExercisesByMuscleGroup = (muscleGroup: string): ExerciseLibraryItem[] => {
  return exerciseLibrary.filter(
    (exercise) =>
      exercise.primaryMuscleGroup === muscleGroup ||
      exercise.secondaryMuscleGroups?.includes(muscleGroup as any)
  );
};

// Helper function to get exercises by equipment
export const getExercisesByEquipment = (equipment: string): ExerciseLibraryItem[] => {
  return exerciseLibrary.filter((exercise) => exercise.equipment === equipment);
};

// Helper function to get exercises by category
export const getExercisesByCategory = (category: string): ExerciseLibraryItem[] => {
  return exerciseLibrary.filter((exercise) => exercise.category === category);
};

// Helper function to search exercises
export const searchExercises = (query: string): ExerciseLibraryItem[] => {
  const lowerQuery = query.toLowerCase();
  return exerciseLibrary.filter(
    (exercise) =>
      exercise.name.toLowerCase().includes(lowerQuery) ||
      exercise.alternativeNames?.some((name) => name.toLowerCase().includes(lowerQuery)) ||
      exercise.primaryMuscleGroup.toLowerCase().includes(lowerQuery) ||
      exercise.tags?.some((tag) => tag.toLowerCase().includes(lowerQuery))
  );
};
